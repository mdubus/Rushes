#include "libft.h"

int		ft_nbrlen(long int n)
{
	int		i;
	int		try;

	if (n < 10 && n > -10)
		return (1);
	if (n <= -1000000000 || n >= 1000000000)
		return (10);
	i = 1;
	try = 9;
	n = ft_positive(n);
	while ((try = try * 10 + 9) < n)
		++i;
	return (i + 1);
}
