#include "../includes/rush.h"

void	init_tab(t_rush *rush)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < rush->size)
	{
		j = 0;
		while (j < rush->size)
			rush->tab[i][j++] = 0;
		i++;
	}
}
